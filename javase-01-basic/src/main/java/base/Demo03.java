package base;

public class Demo03 {
    public static void main(String[] args) {
        //整數拓展 :  進制  二進制0b    十進制    八進制0   十六進制0x
        //
        int i = 10;
        int i2 = 010; //八進制0
        int i3 = 0x10;  //十六進制0x  0~9 A~F 16

        System.out.println(i);
        System.out.println(i2);
        System.out.println(i3);

    }
}
