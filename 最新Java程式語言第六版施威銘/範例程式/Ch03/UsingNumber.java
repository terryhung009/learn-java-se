public class UsingNumber {
  public static void main(String[] argv) {
    short s = -13666;     // 負數
    System.out.println("變數 s = " + s);
    
    long  l = 2_135_482_789L;  // 用 L 標示 long 整數 
    System.out.println("變數 l = " + l);
    
    int  i = 0b1100_0110_0011_1010;  // 底線字元  
    System.out.println("變數 i = " + i);
  }
}
