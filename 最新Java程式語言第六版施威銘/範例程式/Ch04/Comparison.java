public class Comparison {
  public static void main(String[] argv) {
    int i = 4, j = 5;
    System.out.println("當 i = " + i + ", j = " + j);
    System.out.println("i < j  ：" + (i < j));
    System.out.println("i <= j ：" + (i <= j));
    System.out.println("i > j  ：" + (i > j));
    System.out.println("i >= j ：" + (i >= j));
    System.out.println("i == j ：" + (i == j));
    System.out.println("i != j ：" + (i != j));
  }
}
