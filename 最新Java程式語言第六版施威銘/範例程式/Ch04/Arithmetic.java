public class Arithmetic {
  public static void main(String[] argv) {
    int i=13, j=7, result;
    System.out.println(" i= " + i + "  j=" + j);
    
    result = i + j;  // 加
    System.out.println(" i + j：" + result);  
    result = i - j;  // 減
    System.out.println(" i - j：" + result);  
    result = i * j;  // 乘
    System.out.println(" i * j：" + result);  
    result = i / j;  // 除
    System.out.println(" i / j：" + result);  
  }
}